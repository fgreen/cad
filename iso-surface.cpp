// Linearly interpolate the position where an isosurface cuts
// an edge between two vertices, each with their own scalar value
TV3 VertexInterp(const TV3 &p1, const TV3 &p2, T valp1, T valp2) {
  return (p1 + (-valp1 / (valp2 - valp1)) * (p2 - p1));
}

// in order to make this water tight hash intersection with unique edge code
// edge code is formed from indices of base edge vertex and edge dir 0,1,2 for x,y,z
int IsoSurface::intersection(GridCell &Grid, int from, int to, int edge_base, int edge_dir) {
  auto iidx = vec(Grid.ijk[edge_base].x, Grid.ijk[edge_base].y, Grid.ijk[edge_base].z, edge_dir);
  int vidx  = _Intersections.get_default(iidx, -1);
  // printf("LOOKING FOR [%d,%d,%d,%d]: ", iidx[0], iidx[1], iidx[2], iidx[3]);
  if (vidx >= 0) {
    // printf("FOUND  %d\n", vidx);
    return vidx;
  }
  auto val = VertexInterp(Grid.p[from],Grid.p[to],Grid.val[from],Grid.val[to]);
  vidx = _Intersections[iidx] = _Vertices.size();
  // printf("ADDING %d\n", vidx);
  _Vertices.append(val);
  return vidx;
}

int IsoSurface::polygonise(GridCell &Grid) {
  int VertexList[12];
	
  //Determine the index into the edge table which
  //tells us which vertices are inside of the surface
  int CubeIndex = 0;
  if (Grid.val[0] < 0.0) CubeIndex |= 1;
  if (Grid.val[1] < 0.0) CubeIndex |= 2;
  if (Grid.val[2] < 0.0) CubeIndex |= 4;
  if (Grid.val[3] < 0.0) CubeIndex |= 8;
  if (Grid.val[4] < 0.0) CubeIndex |= 16;
  if (Grid.val[5] < 0.0) CubeIndex |= 32;
  if (Grid.val[6] < 0.0) CubeIndex |= 64;
  if (Grid.val[7] < 0.0) CubeIndex |= 128;

  //Cube is entirely in/out of the surface
  if (edgeTable[CubeIndex] == 0)
    return(0);

  //Find the vertices where the surface intersects the cube
  if (edgeTable[CubeIndex] & 1)
    VertexList[0] = intersection(Grid, 0, 1, 0, 0);
  if (edgeTable[CubeIndex] & 2)
    VertexList[1] = intersection(Grid, 1, 2, 1, 1);
  if (edgeTable[CubeIndex] & 4)
    VertexList[2] = intersection(Grid, 2, 3, 3, 0);
  if (edgeTable[CubeIndex] & 8)
    VertexList[3] = intersection(Grid, 3, 0, 0, 1);
  if (edgeTable[CubeIndex] & 16)
    VertexList[4] = intersection(Grid, 4, 5, 4, 0);
  if (edgeTable[CubeIndex] & 32)
    VertexList[5] = intersection(Grid, 5, 6, 5, 1);
  if (edgeTable[CubeIndex] & 64)
    VertexList[6] = intersection(Grid, 6, 7, 7, 0);
  if (edgeTable[CubeIndex] & 128)
    VertexList[7] = intersection(Grid, 7, 4, 4, 1);
  if (edgeTable[CubeIndex] & 256)
    VertexList[8] = intersection(Grid, 0, 4, 0, 2);
  if (edgeTable[CubeIndex] & 512)
    VertexList[9] = intersection(Grid, 1, 5, 1, 2);
  if (edgeTable[CubeIndex] & 1024)
    VertexList[10] = intersection(Grid, 2, 6, 2, 2);
  if (edgeTable[CubeIndex] & 2048)
    VertexList[11] = intersection(Grid, 3, 7, 3, 2);

  int new_triangle_count = 0;
  for (int i=0;triTable[CubeIndex][i]!=-1;i+=3) {
    _Triangles.append(vec(VertexList[triTable[CubeIndex][i+0]],
                          VertexList[triTable[CubeIndex][i+2]],
                          VertexList[triTable[CubeIndex][i+1]]));
    new_triangle_count += 1;
  }
  return new_triangle_count;
}

void IsoSurface::PolygonizeGrids(Array<TV3> points, Array<T> distances, int z) {
  // printf("POLYGONIZE GRIDS %d\n", z);
  int slice_len = _XCount * _YCount;
  for(int x = 0; x < _XCount - 1; x++) {
    for(int y = 0; y < _YCount - 1; y++) {
      GridCell g;
      int z0_off = slice_len * z;
      g.ijk[0] = vec(x+0, y+0, z+0);
      g.ijk[1] = vec(x+1, y+0, z+0);
      g.ijk[2] = vec(x+1, y+1, z+0);
      g.ijk[3] = vec(x+0, y+1, z+0);

      g.p[0] = points[z0_off + _XCount*(y+0)+(x+0)];
      g.p[1] = points[z0_off + _XCount*(y+0)+(x+1)];
      g.p[2] = points[z0_off + _XCount*(y+1)+(x+1)];
      g.p[3] = points[z0_off + _XCount*(y+1)+(x+0)];

      g.val[0] = distances[z0_off + _XCount*(y+0)+(x+0)];
      g.val[1] = distances[z0_off + _XCount*(y+0)+(x+1)];
      g.val[2] = distances[z0_off + _XCount*(y+1)+(x+1)];
      g.val[3] = distances[z0_off + _XCount*(y+1)+(x+0)];

      g.ijk[4] = vec(x+0, y+0, z+1);
      g.ijk[5] = vec(x+1, y+0, z+1);
      g.ijk[6] = vec(x+1, y+1, z+1);
      g.ijk[7] = vec(x+0, y+1, z+1);

      int z1_off = slice_len * (z+1);
      g.p[4] = points[z1_off + _XCount*(y+0)+(x+0)];
      g.p[5] = points[z1_off + _XCount*(y+0)+(x+1)];
      g.p[6] = points[z1_off + _XCount*(y+1)+(x+1)];
      g.p[7] = points[z1_off + _XCount*(y+1)+(x+0)];

      g.val[4] = distances[z1_off + _XCount*(y+0)+(x+0)];
      g.val[5] = distances[z1_off + _XCount*(y+0)+(x+1)];
      g.val[6] = distances[z1_off + _XCount*(y+1)+(x+1)];
      g.val[7] = distances[z1_off + _XCount*(y+1)+(x+0)];

      bool Valid = true;
      for(int VertexIndex = 0; VertexIndex < 8 && Valid; VertexIndex++) {
        if(g.val[VertexIndex] == FLT_MAX) {
          Valid = false;
        }
      }
      if(Valid) {
        polygonise(g);
      }
    }
  }
}

Mesh IsoSurface::IsoApproximate(const TV3 &Start, const TV3 &End, T CellSize, Mesh mesh, T target) {
  _CellSize = CellSize;
  _Start    = Start;
  _End      = End;
  _Diff     = _End - _Start;
  _XCount   = int(_Diff.x / _CellSize) + 1;
  _YCount   = int(_Diff.y / _CellSize) + 1;
  _ZCount   = int(_Diff.z / _CellSize) + 1;

  Array<TV3> points;
  for(int z = 0; z < _ZCount; z++) {
    for(int y = 0; y < _YCount; y++) {
      for(int x = 0; x < _XCount; x++) {
        points.append(vec(_Start.x + _CellSize * (x+0),_Start.y + _CellSize * (y+0),_Start.z + _CellSize * (z+0)));
      }
    }
  }
  // printf("%d POINTS SIZE %d,%d,%d\n", points.size(), _XCount, _YCount, _ZCount);
  auto particles = new_<ParticleTree<TV3>>(points, 4);
  auto tree = new_<SimplexTree<TV3,2>>(mesh.soup, mesh.points, 4);
  Array<T> distances = surface_levelset(*particles, *tree, max(_Diff.x, max(_Diff.y, _Diff.z)), true).x;

  for (int k = 0; k < distances.size(); k++)
    distances[k] -= target;

  for(int z = 0; z < _ZCount - 1; z++) {
    PolygonizeGrids(points, distances, z);
  }

  return fab_mesh(_Triangles, _Vertices);
}

Mesh IsoSurface::IsoApproximate(T BoxSize, T CellSize, Mesh mesh, T target) {
  TV3 End(BoxSize, BoxSize, BoxSize);
  return IsoApproximate(-End, End, CellSize, mesh, target);
}

